
-- ABOUT

KeyMaster es una web para aprender a mejorar tu mecanografía. Ofrece varios modos de juego, un sistema de cuentas para poder guardar tu historial de partidas, las teclas en las que cometes más errores, poder agregar a tus amigos para poder jugar contra ellos... KeyMaster proporciona una retroalimentación en las partidas en tiempo real indicando al jugador errores tipográficos y su velocidad de palabras por minuto a las que es capaz de escribir.



-- WORDSET
De manera predeterminada KeyMaster te ofrece los textos para jugar en español, aunque podrás ir variando también con el inglés o lenguajes de programación



-- MODOS DE JUEGO



-- STATS
wpm - palabras por minuto. Indica al usuario a la velocidad que es capaz de escribir




-- RESULTS SCREEN
Al final de la partida el jugador podrá ver las estadísticas de esta, así como el tiempo que ha tardado en completar el texto, las palabras por minuto y su porcentaje de acierto. Más tarde, si así lo desea desde el apartado de su perfil podrá recuperar y ver las estadísiticas de sus partidas.
